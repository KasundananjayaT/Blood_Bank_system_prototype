    private void BloodTypeSelectionItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_BloodTypeSelectionItemStateChanged
        // TODO add your handling code here:
       
        SelectedBType = BloodTypeSelection.getSelectedItem().toString();
        String OldSelectedBtype= SelectedBType;
        HgetBpacket bp = new HgetBpacket();
        try {
            ABloodTypeSelection.removeAllItems();
            ABloodTypeSelection.addItem(SelectedBType);
            bp.getPreferableType(SelectedBType);
            for(int i=0;i<8;i++){
            if(bp.getBloodType(i) == null ? OldSelectedBtype == null : bp.getBloodType(i).equals(OldSelectedBtype))
            {
                continue;
            }
            else if(bp.getBloodType(i)!=null)
            {
                ABloodTypeSelection.addItem(bp.getBloodType(i));
            }
            else
            {
                break;
            }
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(HGetBP.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(HGetBP.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BloodTypeSelectionItemStateChanged

