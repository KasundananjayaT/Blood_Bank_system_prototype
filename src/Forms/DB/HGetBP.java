    public void rest()
    {
        WnoTxt.setText("");
        BedNoTxt.setText("");
        AgeTxt.setText("");
        WeightTxt.setText("");
    }
     public void validateF(String text,String pattern,JLabel l1){
        Pattern p=Pattern.compile(pattern);
        Matcher m=p.matcher(text);
        if(m.matches()){
                            l1.setText("Valid input");
                            l1.setForeground(Color.green);
        }else{
                            l1.setText("Invalid input");
                            l1.setForeground(Color.RED);
        }
    }
     public void check(){
        if(lblValidateWardNo.getForeground().equals(Color.green)&lblValidateBedNo.getForeground().equals(Color.green)&lblValidateAge.getForeground().equals(Color.green)&lblValidateWeight.getForeground().equals(Color.green)){
            SubmitBtn.setEnabled(true);
        }else{
            SubmitBtn.setEnabled(false);
        }
    }
     public void addError(){
        lblValidateWardNo.setText(" ");
        lblValidateBedNo.setText(" ");
        lblValidateWeight.setText(" ");
        lblValidateAge.setText(" ");
        lblValidateWardNo.setForeground(Color.red);
        lblValidateBedNo.setForeground(Color.red);
        lblValidateWeight.setForeground(Color.red);
        lblValidateWeight.setForeground(Color.red);
    }
