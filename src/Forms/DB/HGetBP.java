    private void SubmitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitBtnActionPerformed
        // TODO add your handling code here:
        String Rtype= RcatogoryBox.getSelectedItem().toString();
        String RBloodType;
        int count= parseInt(CountSpr.getValue().toString());
        if(YesBtn.isSelected())
        {
            RBloodType=BloodTypeSelection.getSelectedItem().toString();
        }
        else
        {
            RBloodType=ABloodTypeSelection.getSelectedItem().toString();
        }
        String Wno=WnoTxt.getText();
        int Bno=parseInt(BedNoTxt.getText());
        int Age=parseInt(AgeTxt.getText());
        double Weight=Double.parseDouble((WeightTxt.getText()));
        HgetBpacket BP = new HgetBpacket();
        try {
            if(count>BP.getCount(RBloodType) && NoBtn.isSelected())
            {
                Component frame = null;
                JOptionPane.showMessageDialog(frame, "Try Alternative Blood Type");
            }
            else
            {
            BP.InsertData(Rtype, RBloodType, count, Wno, Bno, Age, Weight);
            Component frame = null;
            JOptionPane.showMessageDialog(frame, "Data Entered Successfully");
            rest();
            }
        } catch (SQLException ex) {
            Logger.getLogger(HGetBP.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(HGetBP.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_SubmitBtnActionPerformed

